    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            
             Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/AppFutbol");
            PreparedStatement ps=conn.prepareStatement("select * from DATOSFCB");
            ResultSet rs=ps.executeQuery();
            
            ResultSetMetaData rsm=rs.getMetaData();
            ArrayList <Object[]> datosfcb = new ArrayList<>();
            while(rs.next()){
                
                Object[] rows=new Object [rsm.getColumnCount()];
                for (int i = 0; i < rows.length; i++) {
                    rows[i]=rs.getObject(i+1);
                }
                
                datosfcb.add(rows);
                
            }   
            model=(DefaultTableModel)this.jTable1.getModel();
            for (int i = 0; i < datosfcb.size(); i++) {
               model.addRow(datosfcb.get(i));
            }
            
        } catch (Exception e) {
            JOptionPane.showInternalMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

